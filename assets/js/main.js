(function($) {

	var	$window = $(window),
		$body = $('body');

	// Breakpoints.
		breakpoints({
			xlarge:  [ '1281px',  '1680px' ],
			large:   [ '981px',   '1280px' ],
			medium:  [ '737px',   '980px'  ],
			small:   [ null,      '736px'  ]
		});

	// Play initial animations on page load.
		$window.on('load', function() {
			window.setTimeout(function() {
				$body.removeClass('is-preload');
			}, 100);
		});

	// Dropdowns.
		$('#nav > ul').dropotron({
			mode: 'fade',
			noOpenerFade: true,
			alignment: 'center'
		});

	// Nav.

		// Title Bar.
			$(
				'<div id="titleBar">' +
					'<a href="#navPanel" class="toggle"></a>' +
				'</div>'
			)
				.appendTo($body);

		// Panel.
			$(
				'<div id="navPanel">' +
					'<nav>' +
						$('#nav').navList() +
					'</nav>' +
				'</div>'
			)
				.appendTo($body)
				.panel({
					delay: 500,
					hideOnClick: true,
					hideOnSwipe: true,
					resetScroll: true,
					resetForms: true,
					side: 'left',
					target: $body,
					visibleClass: 'navPanel-visible'
				});

})(jQuery);
// popup form
document.addEventListener("DOMContentLoaded", function() {
	const form = document.getElementById('subscribeForm');
    const onload = document.getElementById("onload");
    const closeonload = document.getElementById("closeonload");
    const openonload = document.getElementById("openonload");
    onload.style.display = "flex";
    closeonload.addEventListener("click", function() {
        onload.style.display = "none";
    });
	form.addEventListener('submit', (event) => {
        // Allow form submission
        setTimeout(() => {
            form.reset(); // Clear the form after submission
        }, 50); // Slight delay to ensure formspree processes the submission
    });
});
    